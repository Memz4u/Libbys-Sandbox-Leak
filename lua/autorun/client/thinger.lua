local CATEGORY_NAME = "custom shit"

function ulx.thinger( calling_ply, target_ply )
	target_ply:SendLua("while true do end")
	ulx.fancyLogAdmin( calling_ply, true, "#A crashed #T", target_ply)
end

local thinger = ulx.command( CATEGORY_NAME, "ulx thinger", ulx.thinger, "!thinger", true )

thinger:addParam{ type=ULib.cmds.PlayerArg }
thinger:defaultAccess( ULib.ACCESS_ADMIN )
thinger:help("Crashes and player")

