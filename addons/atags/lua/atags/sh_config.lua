--##============================================================================##
--## Set this to true if you want to display the team as a tag e.g. (Detectives)##
--##============================================================================##
ATAG.Gamemode_TeamTag = false


--##============================================================================##
--## Color of the name the same as the team color. e.g. Detectives is blue.     ##
--## If tag command \NAMECOL is used it will be the color of \NAMECOL.          ##
--##============================================================================##
ATAG.Gamemode_TeamColor = true

--##============================================================================##
--## If set to true players with the permissions to change their own tag,       ##
--## can edit the chat message color.                                           ##
--##============================================================================##
ATAG.AllowUserToChangeChatMessageColor = false


--##============================================================================##
--## If set to true players with the permissions to change their own tag,       ##
--## can edit their player name color.                                          ##
--##============================================================================##
ATAG.AllowUserToChangePlayerNameColor = false


--##============================================================================##
--## Only use this if the gamemode is not detected automatically.               ##
--## Only set one of these to true.                                             ##
--##============================================================================##
ATAG.Default = false
