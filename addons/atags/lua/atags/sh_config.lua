ATAG.Cinema = false
ATAG.Sandbox = false
ATAG.HideAndSeek = false
ATAG.Murder = false
ATAG.NutScript = false

--##============================================================================##
--## Ranks that have access to the Admin panel.                                 ##
--## Giving access to the Admin panel can be done ingame aswell                 ##
--## by giving the player the privilege called 'atags adminpanel'               ##
--##============================================================================##
ATAG.adminPanel_Access = {
	"owner",
