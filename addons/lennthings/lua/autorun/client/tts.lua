timer.Simple(5, function()

hook.Add("OnPlayerChat", "libre_googletts", function(v, text, team)
if string.find(string.lower(text), "/googletts") then
if text[1] == "/" then
local me = LocalPlayer()
if v == me then
print("received message")

-- translate people who say stuff in korean
        chat.AddText("TTS (Text to speech) is enabled, you may disable it by typing /disabletts in chat")
        chat.AddText("You are using the google translate text to speech, so the system can speak words said out to you")
timer.Create("startit_v2", 0.1, 1, function()


hook.Add("OnPlayerChat","libre_speakinggoogles", function(player, text, team, dead)
	sound.PlayURL("http://translate.google.com/translate_tts?ie=UTF-8&client=tw-ob&q="..text.."&tl=en", "mono", function(chan, num, str) boy = chan end)
end)

end)


else

end
end
end
end)



hook.Add("OnPlayerChat", "libre_googlettsdisable", function(v, text, team)
if string.find(string.lower(text), "/disabletts") then
if text[1] == "/" then
local me = LocalPlayer()
if v == me then
print("received message")

-- translate people who say stuff in korean
        chat.AddText("TTS (Text to speech) is disabled, you may enable it by typing /googletts in chat again")
timer.Create("wtfmomentlaunch_v1", 0.2, 1, function()
hook.Remove("OnPlayerChat","libre_speakinggoogles")
RunConsoleCommand("stopsound")
end)


else

chat.AddText("[libby]: google tts loaded")

end
end
end
end)



end)

